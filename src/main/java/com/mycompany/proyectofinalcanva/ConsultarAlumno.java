    private void actualizarTabla() {
        DefaultTableModel data = new DefaultTableModel(new String[]{"Usuario", "Carnet", "Nombre", "Apellido", "Genero"}, ProyectoFinalCanva.alumnos.size());
        jTable1.setModel(data);

        int row = 0;

        for (Alumno e : ProyectoFinalCanva.alumnos) {
            jTable1.setValueAt(e.usuario, row, 0);
            jTable1.setValueAt(e.carne, row, 1);
            jTable1.setValueAt(e.nombre, row, 2);
            jTable1.setValueAt(e.apellido, row, 3);
            jTable1.setValueAt(e.genero, row, 4);

            row++;
        }

    }

    public void escribirArchivoTexto(String rutaConNombre, String contenido) {

        try {
            FileWriter fw = new FileWriter(rutaConNombre);
            PrintWriter pw = new PrintWriter(fw);
            pw.write(contenido);
            pw.close();
            fw.close();
        } catch (IOException e) {
            e.printStackTrace();
        }

    }

