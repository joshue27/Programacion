    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String contenido = "<alumnos>\n";

        for (Alumno e : ProyectoFinalCanva.alumnos) {
            contenido += "\t<alumno>\n";
            contenido += "\t\t<nombre>";
            contenido += e.nombre;
            contenido += "</nombre>\n";
            contenido += "\t\t<apellido>";
            contenido += e.apellido;
            contenido += "</apellido>\n";
            contenido += "\t\t<usuario>";
            contenido += e.usuario;
            contenido += "</usuario>\n";
            contenido += "\t\t<password>";
            contenido += e.password;
            contenido += "</password>\n";
            contenido += "\t\t<carne>";
            contenido += e.carne;
            contenido += "</carne>\n";
            contenido += "\t\t<genero>";
            contenido += e.genero;
            contenido += "</genero>\n";
            contenido += "\t</alumno>\n";
        }
        contenido += "</alumnos>";
        JFileChooser archivo = new JFileChooser();
        int retorno = archivo.showDialog(this, "Guardar");
        if (retorno == JFileChooser.APPROVE_OPTION) {
            escribirArchivoTexto(archivo.getSelectedFile().toPath().toString(), contenido);
            JOptionPane.showMessageDialog(null, "Archivo Guardado Exitosamente");
        }
    }//GEN-LAST:event_jButton2ActionPerformed


